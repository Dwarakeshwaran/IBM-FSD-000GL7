package com.practice.abstractclass;

public class Goku extends SuperPower{

	@Override
	public void powerUp() {
		System.out.println("Goku Powers up over 9000");
		
	}

	@Override
	public void saveTheWorld() {
		System.out.println("Goku Saves the world by killing Kid Buu");
		
	}

	@Override
	public void trainHard() {
		System.out.println("Goku trained hard");
		
	}
	
}